public class LP8Strings {
    public static void main(String[] args) {
        String hello = "Hello";
        String world = "World";
        String result = hello + " " + world + "!";
        System.out.println(result + "\n");

        // Самостоятельно
        String name = "Джон";
        int age = 37;
        String helloPattern = "Привет, \"" + name + "\"! \nТебе " + age + " лет.";
        System.out.println(helloPattern);
    }
}
